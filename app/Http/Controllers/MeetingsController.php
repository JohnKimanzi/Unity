    {
       Meeting::create([
           'lead_id'=>$request->get('lead_id'),
           'title'=>$request->get('title'),
           'description'=>$request->get('description'),
           'from'=>$request->get('meeting_starts'),
           'to'=>$request->get('meeting_ends'),
           'reminder_mode'=>$request->get('reminder_mode'),
           'reminder_time'=>$request->get('reminder_time'),
           

       ]); 

       return back()
                ->with('success','Meeting Now Scheduled.');
    }

